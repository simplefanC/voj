    /**
     * @param colorInt RGB
     * @return
     */
    private static boolean isBlack(int colorInt) {
        Color color = new Color(colorInt);
        if (color.getRed() + color.getGreen() + color.getBlue() <= 500) {
            return true;
        }
        return false;
    }

    /**
     * @param image 
     * @throws IOException
     */
    private static void printImage(BufferedImage image) {
        int h = image.getHeight();
        int w = image.getWidth();

        // 
        for (int y = 0; y < h; y++) {
            System.out.printf("\"");
            for (int x = 0; x < w; x++) {
                if (isBlack(image.getRGB(x, y))) {
                    System.out.print("#");
                } else {
                    System.out.print(".");
                }
            }
            System.out.printf("%s", y == h - 1 ? "\"" : "\",");
            System.out.println();
        }
    }

    /**
     * @param file gif
     * @throws Exception
     */
    private static List<BufferedImage> splitGif(File file) throws IOException {
        InputStream in = new FileInputStream(file);
        GifDecoder d = new GifDecoder();
        d.read(in);
        int num = d.getFrameCount();
        List<BufferedImage> subImgs = new ArrayList<>();
        for (int i = 0; i < num; i++) {
            // 
            // 3 2 1 0
            BufferedImage frame = d.getFrame(num - i - 1);
            // printImage(frame);
            subImgs.add(frame.getSubimage(7 + i * 36, 5, 30, 27));
        }
        in.close();
        return subImgs;
    }

    /**
     * @param image 
     * @return
     */
    private static char recognizeSymbol(BufferedImage image) {
        int h = image.getHeight();
        int w = image.getWidth();

        int minDiff = 999999;
        char symAns = 0;
        // 
        for (int i = 0; i < 10; i++) {
            int curDiff = 0;
            for (int y = 0; y < h; y++) {
                for (int x = 0; x < w; x++) {
                    boolean pixel1 = digitals[i][y].charAt(x) == '#';
                    boolean pixel2 = isBlack(image.getRGB(x, y));
                    if (pixel1 != pixel2) {
                        ++curDiff;
                    }
                }
            }
            if (curDiff < minDiff) {
                minDiff = curDiff;
                symAns = (char) ('0' + i);
            }
            if (minDiff < 5) {
                return symAns;
            }
        }

        // 
        for (int i = 0; i < 26; i++) {
            int curDiff = 0;
            for (int y = 0; y < h; y++) {
                for (int x = 0; x < w; x++) {
                    boolean pixel1 = upper[i][y].charAt(x) == '#';
                    boolean pixel2 = isBlack(image.getRGB(x, y));
                    if (pixel1 != pixel2) {
                        ++curDiff;
                    }
                }
            }
            if (curDiff < minDiff) {
                minDiff = curDiff;
                symAns = (char) ('A' + i);
            }
            if (minDiff < 5) {
                return symAns;
            }
        }

        // 
        for (int i = 0; i < 26; i++) {
            int curDiff = 0;
            for (int y = 0; y < h; y++) {
                for (int x = 0; x < w; x++) {
                    boolean pixel1 = lower[i][y].charAt(x) == '#';
                    boolean pixel2 = isBlack(image.getRGB(x, y));
                    if (pixel1 != pixel2) {
                        ++curDiff;
                    }
                }
            }
            if (curDiff < minDiff) {
                minDiff = curDiff;
                symAns = (char) ('a' + i);
            }
            if (minDiff < 5) {
                return symAns;
            }
        }

        return symAns;
    }

    /**
     * @param file 
     * @return
     */
    public static String recognize(File file) throws IOException {
        StringBuilder ans = new StringBuilder();

        List<BufferedImage> subImgs = splitGif(file);
        for (BufferedImage subImg : subImgs) {
            ans.append(recognizeSymbol(subImg));
        }
        System.out.printf("regonize: %s\n", ans);
        return ans.toString();
    }

